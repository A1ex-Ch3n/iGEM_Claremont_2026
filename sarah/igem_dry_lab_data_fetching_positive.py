def fetch_host_batch(id_batch):
    # 將一個 batch (例如 50 個 ID) 組合為字串 "ID1,ID2,ID3..." 進行一次性查詢，這比一個個查快 50 倍
    query_ids = ",".join(id_batch)
    results = []

    try:
        # 請求下載 GenBank 格式
        handle = Entrez.efetch(db="nucleotide", id=query_ids, rettype="gb", retmode="text")
        records = list(SeqIO.parse(handle, "genbank"))
        handle.close()

        for record in records:
            phage_name = record.description
            acc_id = record.id
            host = "Unknown" # 預設值

            # 在 GenBank 格式的 features 中尋找 source 標籤
            for feature in record.features:
                if feature.type == "source":
                    # 嘗試抓取 host 或 lab_host
                    host_info = feature.qualifiers.get("host", feature.qualifiers.get("lab_host", ["Unknown"]))
                    host = host_info[0] if host_info else "Unknown"
                    break

            results.append({
                "Phage": phage_name,
                "Accession": acc_id,
                "Host": host,
                "Affinity": 1 if host != "Unknown" else "", # NCBI 記錄的都是有感染能力的 (Affinity = 1)
                "Source": f"NCBI: {acc_id}"
            })

    except Exception as e:
        print(f"批次下載發生錯誤: {e}")
        # 如果整批失敗，就記錄為 Error 方便事後除錯
        for acc in id_batch:
            results.append({"Phage": "ERROR", "Accession": acc, "Host": "ERROR", "Affinity": "", "Source": "ERROR"})

    return results

# --- 4. 執行批次下載與進度條 ---
