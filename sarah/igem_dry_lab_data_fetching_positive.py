df_raw = pd.read_csv(input_file)

# 清理无效资料
df_clean = df_raw[~df_raw['Host_Name'].isin(['Unknown', 'ERROR', 'No Complete Genome Found'])].copy()
df_clean['Affinity'] = pd.to_numeric(df_clean['Affinity'], errors='coerce')

# --- 2. 建立 Phage 的「名称 + Accession」复合标签（用于行） ---
df_clean['Phage_Label'] = df_clean['Phage'] + ' [' + df_clean['Phage_Accession'] + ']'

# --- 3. Pivot Table（用复合标签作为行） ---
print("正在转换为 2D 感染矩阵...")
matrix_df = df_clean.pivot_table(
    index='Phage_Label',
    columns='Host_Name',
    values='Affinity',
    aggfunc='max'
)
matrix_df = matrix_df.sort_index(axis=0).sort_index(axis=1)

# --- 4. 在列标题加入 Host Accession ---
