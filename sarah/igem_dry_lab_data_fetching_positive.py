def generate_accession_list(query):
    print(f"正在 NCBI Nucleotide 執行搜尋...\n關鍵字: {query}")

    # 發送搜尋請求 (最多抓取 1000 筆，通常 Xanthomonas 噬菌體大約幾百個)
    try:
        search_handle = Entrez.esearch(db="nucleotide", term=query, retmax=1000)
        search_record = Entrez.read(search_handle)
        id_list = search_record["IdList"]
        search_handle.close()
    except Exception as e:
        print(f"搜尋失敗: {e}")
        return []

    total_found = len(id_list)
    print(f"成功找到 {total_found} 個潛在的噬菌體 ID！正在下載摘要資料...")

    results = []

    # 如果有找到資料，批次獲取摘要 (每次抓 200 筆，避免超載)
    if total_found > 0:
        batch_size = 200
        for start in range(0, total_found, batch_size):
            end = min(total_found, start + batch_size)
            batch_ids = id_list[start:end]

            print(f"處理進度: {start+1} 到 {end}...")

            try:
                summary_handle = Entrez.esummary(db="nucleotide", id=",".join(batch_ids))
                summaries = Entrez.read(summary_handle)
                summary_handle.close()

                for item in summaries:
                    results.append({
                        "Accession": item["Caption"],      # 這是你要的 MG944227 等 ID
                        "Description": item["Title"],      # 標題通常包含 Phage 名稱
                        "Length": item["Length"],          # 基因組長度 (可藉此過濾掉太短的片段)
                        "UpdateDate": item["UpdateDate"]   # 了解這是不是最新的資料
                    })
            except Exception as e:
                print(f"獲取摘要時發生錯誤: {e}")

            # 禮貌性延遲，保護你的 IP 不被 NCBI 封鎖
            time.sleep(1)

    return results

