import pandas as pd
from Bio import Entrez, SeqIO
import time

# --- 1. 基本設定 ---
Entrez.email = "cchen29@cmc.edu"
input_csv = "xanthomonas_phages_accession_list.csv"
output_csv = "phage_host_matrix_with_ids.csv"

# 建立一個「快取字典」，避免重複查詢相同的宿主 ID
host_id_cache = {"Unknown": "Unknown"}

# --- 2. 輔助函數：根據細菌名稱，尋找最具代表性的 Accession ID ---
def get_host_accession(host_name):
    # 如果這個細菌我們已經查過了，直接從記憶裡拿出來
    if host_name in host_id_cache:
        return host_id_cache[host_name]

    print(f"  🔍 發現新宿主 '{host_name}'，正在尋找其代表性 Accession ID...")

    # 搜尋條件：找這個細菌的「完整基因組」，並且最好是 RefSeq (官方參考序列)
    # 加上 srcdb_refseq[PROP] 可以優先找到 NC_ 開頭的高品質序列
    query = f'"{host_name}"[Organism] AND "complete genome"[All Fields] AND srcdb_refseq[PROP]'

    try:
        search_handle = Entrez.esearch(db="nucleotide", term=query, retmax=1)
        search_record = Entrez.read(search_handle)
        search_handle.close()

        id_list = search_record["IdList"]

        if id_list:
            # 如果有找到 UID，再轉換成我們常見的 Accession ID (NC_xxxx 或 NZ_xxxx)
            summary_handle = Entrez.esummary(db="nucleotide", id=id_list[0])
            summary_record = Entrez.read(summary_handle)
            summary_handle.close()

            host_acc = summary_record[0]["Caption"]
            host_id_cache[host_name] = host_acc # 存入快取
            return host_acc
        else:
            # 如果加上 RefSeq 找不到，放寬條件再找一次
            fallback_query = f'"{host_name}"[Organism] AND "complete genome"[All Fields]'
            fallback_handle = Entrez.esearch(db="nucleotide", term=fallback_query, retmax=1)
            fallback_record = Entrez.read(fallback_handle)
            fallback_handle.close()

            if fallback_record["IdList"]:
                sum_handle = Entrez.esummary(db="nucleotide", id=fallback_record["IdList"][0])
                sum_rec = Entrez.read(sum_handle)
                sum_handle.close()
                host_acc = sum_rec[0]["Caption"]
                host_id_cache[host_name] = host_acc
                return host_acc
            else:
                host_id_cache[host_name] = "No Complete Genome Found"
                return "No Complete Genome Found"

    except Exception as e:
        print(f"  ❌ 尋找宿主 ID 時發生錯誤: {e}")
        return "ERROR"
    finally:
        time.sleep(0.5) # 避免請求過快

# --- 3. 讀取與核心批次爬取函數 ---
print(f"正在讀取 {input_csv} ...")
df = pd.read_csv(input_csv)
accession_list = df['Accession'].tolist()
total_ids = len(accession_list)

def fetch_phage_and_host_data(id_batch):
    query_ids = ",".join(id_batch)
    results = []

    try:
        handle = Entrez.efetch(db="nucleotide", id=query_ids, rettype="gb", retmode="text")
        records = list(SeqIO.parse(handle, "genbank"))
        handle.close()

        for record in records:
            phage_name = record.description
            acc_id = record.id
            host_name = "Unknown"

            for feature in record.features:
                if feature.type == "source":
                    host_info = feature.qualifiers.get("host", feature.qualifiers.get("lab_host", ["Unknown"]))
                    host_name = host_info[0] if host_info else "Unknown"
                    break

            # 呼叫輔助函數，獲取 Host 的 Accession ID
            host_acc_id = get_host_accession(host_name)

            results.append({
                "Phage": phage_name,
                "Phage_Accession": acc_id,
                "Host_Name": host_name,
                "Host_Accession": host_acc_id,
                "Affinity": 1 if host_name != "Unknown" else "",
                "Source": f"NCBI: {acc_id}"
            })

    except Exception as e:
        print(f"批次下載發生錯誤: {e}")
    return results

# --- 4. 執行迴圈 ---
batch_size = 50
all_results = []

for i in range(0, total_ids, batch_size):
    batch = accession_list[i : i + batch_size]
    print(f"\n▶ 正在爬取 Phage 批次: {i + 1} ~ {min(i + batch_size, total_ids)} / {total_ids}")

    batch_data = fetch_phage_and_host_data(batch)
    all_results.extend(batch_data)
    time.sleep(1)

# --- 5. 輸出結果 ---
final_df = pd.DataFrame(all_results)
